function [ q ] = q_seq_rot( b, r )
q=normalize_q([quatmult(calc_q32(b,q2a([1 0 0 0])*r),[1 0 0 0]);...
               quatmult(calc_q32(b,q2a([0 1 0 0])*r),[0 1 0 0]);...
               quatmult(calc_q32(b,q2a([0 0 1 0])*r),[0 0 1 0]);...
               quatmult(calc_q32(b,q2a([0 0 0 1])*r),[0 0 0 1])]);
end