function [ q ] = q_seq_rot( r, b )
q=normalize_q([quatmult(calc_q32(r,q2a([1 0 0 0])*b),[1 0 0 0]);...
               quatmult(calc_q32(r,q2a([0 1 0 0])*b),[0 1 0 0]);...
               quatmult(calc_q32(r,q2a([0 0 1 0])*b),[0 0 1 0]);...
               quatmult(calc_q32(r,q2a([0 0 0 1])*b),[0 0 0 1])]);
end